package com.jits.library;

import java.util.Comparator;

import com.jits.shipping.Parcel;

public class ShippingTypeComparator implements Comparator<Parcel> {
	
		@Override
		public int compare(Parcel first, Parcel second){
			return first.getShippingType().compareTo(second.getShippingType());
		}

}
